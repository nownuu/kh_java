package condition2;
import java.lang.*;

public class Test01 {
	public static void main(String[] args) {
		
		int value = 10;
		switch(value) {
		case 5 :
			System.out.println("5");
			break;
		case 10:
			System.out.println("10");
			break;
			default:
				System.out.println("기타");
				break;
		}
	}
}
